package com.example.bkt3;

public class ViDu {
    String tenViDu, cauViDu;

    public ViDu() {
    }

    public ViDu(String tenViDu, String cauViDu) {
        this.tenViDu = tenViDu;
        this.cauViDu = cauViDu;
    }

    public String getTenViDu() {
        return tenViDu;
    }

    public void setTenViDu(String tenViDu) {
        this.tenViDu = tenViDu;
    }

    public String getCauViDu() {
        return cauViDu;
    }

    public void setCauViDu(String cauViDu) {
        this.cauViDu = cauViDu;
    }
}
